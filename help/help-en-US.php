
<div>
    <h2>Description</h2>
        <p>yTicket for WP allows you to connect your website to your platform help desk yTicket.<br />yTicket for WP is a plugin that allows you to interface your own website created with the WP platform for Help Desk Ticketing System yTicket.<br />With this plugin, you can:<ul>
                <li>1) create a form with which customers can open a ticket from a page of the website</li>
                <li>2) create a form with which customers can check the processing status of a ticket</li>
            </ul>
        </p>

        <h3>How to use</h3>
        <h4>Open a new ticket</h4>
        <p>To allow your customers to open a support request (ticket) made ​​from your site with wordpress, simply create a new post or page and within the content where you want it to appear in the form, insert the following code {yticket}</p>
        <h4>Status Monitor ticket processing</h4>
        <p>To allow your customers to check the processing status of a ticket from your web site developed with wordpress, simply insert the content of a post or a page the following code {yticket-check}</p>
        
        <h3>What you need</h3>
        <p>To use this plugin you must:<ul>
            <li>a) have activated a floor yTicket on the site <a href="http://ticketingsystem.it" target="_blank">ticketingsystem.it</a> or <a href="http://cloudbox.it/software/yticket-help-desk-software.html" target="_blank">CloudBox.it</a></li></li>
            <li>b) have a site made with WordPress</li>
        </ul></p>
        
