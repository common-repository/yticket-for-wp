    <h2>Descrizione</h2>
        <p>yTicket per WP permette di collegare il vostro sito web alla vostra piattaforma di help desk yTicket.<br />yTicket per WP è un plugin che permette di interfacciare il proprio sito web, creato con la piattaforma WP, con il sistema di Help Desk  e Ticketing System yTicket.<br />Con questo plugin, è possibile:<ul>
                <li>1) creare un modulo con il quale i clienti possono aprire un ticket da una pagina del sito web</li>
                <li>2) creare un modulo con il quale i clienti possono verificare lo stato di elaborazione di un ticket</li>
            </ul>
        </p>

        <h3>Come si usa</h3>
        <h4>Apertura di un nuovo ticket</h4>
        <p>Per permettere ai vostri clienti di aprire una richiesta di supporto (ticket) dal vostro sito fatto con wordpress,  è sufficiente creare un nuovo post o pagina e all'interno del contenuto, dove volete che venga mostrato il form,  inserire il seguente codice {yticket}</p>
        <h4>Controllo stato lavorazione ticket</h4>
        <p>Per permettere ai vostri clienti di controllare lo stato di lavorazione di un ticket dal vostro sito web sviluppato con wordpress, è sufficiente inserire nel contenuto di un post o di una pagina il seguente codice {yticket-check}</p>
        
        <h3>Cosa serve</h3>
        <p>Per utilizzare questo plugin è necessario:<ul>
            <li>a) avere attivato un piano yTicket su <a href="http://ticketingsystem.it" target="_blank">ticketingsystem.it</a> o su <a href="http://cloudbox.it/software/yticket-help-desk-software.html" target="_blank">CloudBox.it</a></li>
            <li>b) avere un sito fatto con Wordpress</li>
        </ul></p>
        
